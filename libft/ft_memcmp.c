#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned int		i;
	unsigned const char	*x;
	unsigned const char	*y;

	i = 0;
	x = s1;
	y = s2;
	if (n == 0)
		return (0);
	while (i < n)
	{
		if (x[i] != y[i])
			return (x[i] - y[i]);
		i++;
	}
	return (0);
}
